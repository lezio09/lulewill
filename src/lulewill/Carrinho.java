package lulewill;

public class Carrinho {

	private String Nome;
	private String Modelo;
	private int Preco;
	public String getNome() {
		
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getModelo() {
		return Modelo;
	}
	public void setModelo(String modelo) {
		Modelo = modelo;
	}
	public int getPreco() {
		return Preco;
	}
	public void setPreco(int preco) {
		Preco = preco;
	}
	
	
}
