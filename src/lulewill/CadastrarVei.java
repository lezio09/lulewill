package lulewill;
//Clase de cadastro dos veiculos
public class CadastrarVei {
	
	private String modelo;//digitar o tipo do modelo do veiculo cadastrado 
	private String ano;//digitar o ano do veiculo cadastrado
	private String nome;//digitar o nome da marca do carro
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getAno() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
